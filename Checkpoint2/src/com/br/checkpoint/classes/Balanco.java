package com.br.checkpoint.classes;

public class Balanco {
	String ativos;
	String passivos;
	String receitas;
	String lucro;
	String prejuizo;
	public String getAtivos() {
		return ativos;
	}
	public void setAtivos(String ativos) {
		this.ativos = ativos;
	}
	public String getPassivos() {
		return passivos;
	}
	public void setPassivos(String passivos) {
		this.passivos = passivos;
	}
	public String getReceitas() {
		return receitas;
	}
	public void setReceitas(String receitas) {
		this.receitas = receitas;
	}
	public String getLucro() {
		return lucro;
	}
	public void setLucro(String lucro) {
		this.lucro = lucro;
	}
	public String getPrejuizo() {
		return prejuizo;
	}
	public void setPrejuizo(String prejuizo) {
		this.prejuizo = prejuizo;
	}
}
